#Calculadora simples otimizada

def soma(n1, n2):
    return n1 + n2

def sub(n1, n2):
    return n1 - n2

def div(n1, n2):
    return n1/n2

def mult(n1, n2):
    return n1 * n2

def rad(n1):
    return n1 ** (1/2)

